-- Предел цены лида для оценки креативов (в валюте рекламного кабинета).
--
-- Правило владельца: лид дороже лимита — креатив выключаем. Держим числом на
-- проекте, чтобы поднять планку без правки кода, когда цены вырастут.
-- По умолчанию 3 (доллара).
alter table projects add column cpl_limit numeric not null default 3
  check (cpl_limit > 0);
