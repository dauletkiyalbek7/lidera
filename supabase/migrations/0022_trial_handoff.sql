-- Передача пробного урока продажнику (ТЗ, Блок 2: Пробные уроки, Кабинет продажника).
--
-- Менеджер продал пробный (990 ₸) и назначил дату/время — лид уходит в очередь
-- продажнику по кругу. Продажник проводит онлайн-урок и закрывает продажу курса.
-- Держим это полями на leads, а не отдельной таблицей: воронка уже лид-центрична
-- (статус, ответственный), а пробный — её этап, а не самостоятельная сущность.
alter table leads
  add column salesperson_id uuid references profiles(id),  -- кто проводит пробный
  add column trial_at        timestamptz,                  -- назначенные дата и время
  add column trial_paid_at   timestamptz;                  -- когда оплатили пробный

-- Очередь продажника: быстро находим его незакрытые пробные для раздачи по кругу.
create index leads_salesperson_idx on leads (project_id, salesperson_id);
